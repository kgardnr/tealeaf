# 1. what is != and where should you use it?
# != is a comparison method meaning "not equal to"; you would use it to compare 2 operands like a != b, which would return true if they are not equal

# 2. put ! before something, like !user_name
# means it will return the opposite value, so if it's true it will return false

# 3. put ! after something, like str.gsub()!
# this makes it a destructive change

# 4. put ? before something
# in a ternary operator, use ? before the result if the statement is true

# 5. put ? after something
# in a ternary operatoe, ? comes after the condition you're evaluating to be true or false; it also can be part of a method

# 6. put !! before something, like !!user_name
# this evaluates to either true or false; it is the opposite of !user_name
